package practicaEDT2;

public class Empleado {
	private String nombre;
	private String apellido;
	private String cargo;
	private int edad;

	public Empleado(String nombre, String apellido, String cargo, int edad) {
		this.nombre = nombre;
		this.apellido = apellido;
		this.cargo = cargo;
		this.edad = edad;
	}
}